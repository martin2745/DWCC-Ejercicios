// Operación: 125 / 8
const resultado1 = 125 >> 3;
alert(`125 / 8 = ${resultado1}`);

// Operación: 40 * 4
const resultado2 = 40 << 2;
alert(`40 x 4 = ${resultado2}`);

// Operación: 25 / 2
const resultado3 = 25 >> 1;
alert(`25 / 2 = ${resultado3}`);

// Operación: 10 * 16
const resultado4 = 10 << 4;
alert(`10 x 16 = ${resultado4}`);

